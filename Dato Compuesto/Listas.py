lista = ["Naranja", 28, False, "Plátano"]
print(type(lista[0]))  # Tipo de "Naranja"
print(type(lista[1]))  # Tipo de 28
print(type(lista[2]))  # Tipo de False
print(type(lista[3]))  # Tipo de "Plátano"

lista[1] = "Manzana"   # Cambiamos el valor en la posición 1
print(type(lista[1]))   # Tipo de "Manzana"

print(lista)            # Mostramos la lista final
